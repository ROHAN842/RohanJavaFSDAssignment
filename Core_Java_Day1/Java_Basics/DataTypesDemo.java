package datatype;

public class DataTypesDemo {
	
	public static void main(String args[])
	{
		int amount = 10;
		float balance = 453.6f;
		double remaing_bal = 8745.21;
		char mychar = 'c';
		byte b = 10;
		short smalldata = 10000;
		boolean isvalid = true;
		long longestdata = 50000000L;
		
		
	 System.out.println("Value of int data type is as : " + amount);
	 System.out.println("Value of float data type is as : " + balance);
	 System.out.println("Value of double data type is as : " + remaing_bal);
	 System.out.println("Value of char data type is as : " + mychar);
	 System.out.println("Value of byte data type is as : " + b);
	 System.out.println("Value of short data type is as : " + smalldata);
	 System.out.println("Value of boolean data type is as : " + isvalid);
	 System.out.println("Value of long data type is as : " + longestdata);

	}

}
